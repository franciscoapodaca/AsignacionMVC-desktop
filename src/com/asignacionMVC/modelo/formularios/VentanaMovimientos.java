    public void mostrarDatos(String valor) {
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Folio");
            modelo.addColumn("Fecha");
            modelo.addColumn("Tipo Mov");
            modelo.addColumn("Cantidad");
            modelo.addColumn("Clave Articulo");

            tablaMovimientos.setModel(modelo);

            String sql = "";
            if (valor.equals("")) {
                sql = "SELECT * FROM Movimientos";
            } else {
                sql = "SELECT * FROM Movimientos WHERE folio ='" + valor + "'";
            }
            String[] datos = new String[5];
            conexion();
            try {
                Statement st = cnn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                while (rs.next()) {
                    datos[0] = rs.getString(1);
                    datos[1] = rs.getString(2);
                    datos[2] = rs.getString(3);
                    datos[3] = rs.getString(4);
                    datos[4] = rs.getString(5);
                    modelo.addRow(datos);
                }
                tablaMovimientos.setModel(modelo);
            } catch (SQLException ex) {
                Logger.getLogger(VentanaMovimientos.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (Exception ex) {
            Logger.getLogger(VentanaMovimientos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
