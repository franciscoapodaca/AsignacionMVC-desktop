    private void tablaMovimientosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMovimientosMousePressed
        int fila = tablaMovimientos.getSelectedRow();
        if (fila >= 0) {

            campoFolio.setText(tablaMovimientos.getValueAt(fila, 0).toString());
            campoFecha.setText(tablaMovimientos.getValueAt(fila, 1).toString());
            campoMovimiento.setText(tablaMovimientos.getValueAt(fila, 2).toString());
            campoCantidad.setText(tablaMovimientos.getValueAt(fila, 3).toString());
            campoClaveArticulo.setText(tablaMovimientos.getValueAt(fila, 4).toString());
            botonAgregar.setEnabled(false);
            botonEliminar.setEnabled(true);
            botonActualizar.setEnabled(true);
            campoFolio.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "NO A SELECCIONADO NINGUN MOVIMIENTO");
        }
    }//GEN-LAST:event_tablaMovimientosMousePressed

    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        if (campoFolio.getText().trim().length() == 0 || campoCantidad.getText().trim().length() == 0
                || campoClaveArticulo.getText().trim().length() == 0 || campoFecha.getText().trim().length() == 0
                || campoMovimiento.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "VERIFIQUE NO EXISTAN CAMPOS VACIOS");
        } else {
            dto.setFolio(campoFolio.getText());
            dto.setFecha(new Fecha(campoFecha.getText()));
            dto.setTipoMov(campoMovimiento.getText());
            dto.setCantidad(Integer.parseInt(campoCantidad.getText()));
            dto.setClaveArticulo(campoClaveArticulo.getText());
            try {
                delegateMovimientos.crearMovimientosDelegate(dto);
                JOptionPane.showMessageDialog(null, "Movimiento Agregado");
                limpiarCampos();
                mostrarDatos("");
                cnn.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "EL ID DEL MOVIMIENTO YA EXISTE ", "¡ERROR!", 0);
                campoFolio.setText("");
            }
        }
    }//GEN-LAST:event_botonAgregarActionPerformed

